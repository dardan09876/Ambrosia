// Helper to get base item
function getBaseItem(itemId) {
    return BASE_ITEMS[itemId];
}

// Helper to get items by category
function getBaseItemsByCategory(category) {
    return Object.values(BASE_ITEMS).filter(item => item.category === category);
}

// Helper to get items by tier range
function getBaseItemsByTierRange(minTier, maxTier) {
    return Object.values(BASE_ITEMS).filter(
        item => item.tierMin <= maxTier && item.tierMax >= minTier
    );
}

// Helper to get items by tags
function getBaseItemsByTags(tags) {
    return Object.values(BASE_ITEMS).filter(item =>
        tags.some(tag => item.tags.includes(tag))
    );
}
